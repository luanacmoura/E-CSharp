using System;

namespace calc_met.classes
{
    public class calculo
    {

        public float somar(float x, float y) {
            return x + y;
        }

        public float subtrair(float x, float y){
            return x - y;
        }

        public float multiplicar(float x, float y){
            return x*y;
        }

        public float dividir(float x, float y){
            return x/y;
        }
    }
}